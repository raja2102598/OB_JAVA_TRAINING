package com.onebill.corejava.basics.day2;

public class Switch {

	public static void main(String[] args) {
		for(int i=0;i<=4;i++) {
			switch(i) {
			case 0: System.out.println("For 0");
					break;
			case 1: System.out.println("For 1");
					break;
			case 2: System.out.println("For 2");
					break;
			case 3: System.out.println("For 3");
					break;
			default:System.out.println("Default case");
			}
				
		}
	}

}
